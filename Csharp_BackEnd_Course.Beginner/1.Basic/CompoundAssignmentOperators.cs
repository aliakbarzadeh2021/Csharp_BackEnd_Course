namespace Csharp_BackEnd_Course.Beginner._1.Basic
{
    internal class CompoundAssignmentOperators
    {
        public static void Run()
        {
            // Compound Assignment Operators
            int value = 10;
            value += 10;        // value = 20
            value -= 5;         // value = 15
            value *= 10;        // value = 150
            value /= 3;         // value = 50
            value %= 8;         // value = 2
        }
    }
}
